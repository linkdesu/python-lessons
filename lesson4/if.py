# 当条件为真，执行 if 代码块的代码。
if True:
    print('This code will be execute.')

print('This code will be execute.')

if True or False:
    print('This code will be execute.')

if 1 <= 2:
    print('This code will be execute.')

# 当条件为假，不执行 if 代码块的代码。
if False:
    print('This code will not be execute.')

if 2 > 2:
    print('This code will not be execute.')

if 10 % 3 == 0:
    print('This code will not be execute.')